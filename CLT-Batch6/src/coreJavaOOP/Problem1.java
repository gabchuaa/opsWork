package coreJavaOOP;

public class Problem1 {
	public static void main(String []args) {
		
		int i,j;
		for(i=1; i<=5; i++) {//start of loop
							 //loop for 5 time
			
			for(j=0; j<i;j++) {
				System.out.print("* ");//increment per loop
			}
			System.out.println("\n");
		}
	}

}//end of Problem1
